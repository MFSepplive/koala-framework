"use strict";
const child_process = require('child_process');
module.exports = function() {
    let out = child_process.spawnSync('php', ['bootstrap.php', 'webpack-config']);
    if (out.status !== 0) {
        throw new Error('webpack-config failed:\n'+out.stdoutt+out.stderr);
    }
    out = JSON.parse(out.stdout);
    return out;
}
