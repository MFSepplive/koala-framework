var cache = {};

module.exports = function(content) {
    cache[this.data.cacheId] = content;
    return content;
};
module.exports.pitch = function(remainingRequest, precedingRequest, data) {
    data.cacheId = remainingRequest;
    if (cache[this.data.cacheId]) {
        return cache[this.data.cacheId];
    }
};
